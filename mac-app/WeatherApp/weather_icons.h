static void draw_icon(unsigned char *icon_data, int x, int y) {
    int row, col;

    for (row = 0; row < ICON_SMALL_HEIGHT; row++) {
        for (col = 0; col < ICON_SMALL_WIDTH; col++) {
            int byte_index = (row * ICON_SMALL_WIDTH + col) / 8;
            int bit_index = 7 - (col % 8);

            if (icon_data[byte_index] & (1 << bit_index)) {
                MoveTo(x + col, y + row);
                LineTo(x + col, y + row);
            }
        }
    }
}

static void draw_icon_large(unsigned char *icon_data, int x, int y) {
    int row, col;

    for (row = 0; row < ICON_LARGE_HEIGHT; row++) {
        for (col = 0; col < ICON_LARGE_WIDTH; col++) {
            int byte_index = (row * ICON_LARGE_WIDTH + col) / 8;
            int bit_index = 7 - (col % 8);

            if (icon_data[byte_index] & (1 << bit_index)) {
                MoveTo(x + col, y + row);
                LineTo(x + col, y + row);
            }
        }
    }
}

